export function calculateAge(dobString: string, now: Date = new Date()): Age | null {
  if (!dobString) return null;
  const dob = new Date(dobString);
  if (Number.isNaN(dob.getTime())) return null;

  // Normalize times to avoid timezone edge cases
  const start = new Date(dob.getFullYear(), dob.getMonth(), dob.getDate());
  const end = new Date(now.getFullYear(), now.getMonth(), now.getDate());

  if (end < start) return null;

  const diffMs = end.getTime() - start.getTime();
  const totalDays = Math.floor(diffMs / (1000 * 60 * 60 * 24));

  let years = end.getFullYear() - start.getFullYear();
  let months = end.getMonth() - start.getMonth();
  let days = end.getDate() - start.getDate();

  if (days < 0) {
    months -= 1;
    const prevMonth = new Date(end.getFullYear(), end.getMonth(), 0);
    days += prevMonth.getDate();
  }

  if (months < 0) {
    years -= 1;
    months += 12;
  }

  return { years, months, days, totalDays };
}

export function getStageForAge(age: Age | null): AgeStage | null {
  if (!age) return null;
  const day = age.totalDays;
  const found = ageStages.find((stage) => day >= stage.minDays && day <= stage.maxDays);
  if (found) return found;
  // For age beyond defined range, return the last stage as a generic guide
  return ageStages[ageStages.length - 1] ?? null;
}

